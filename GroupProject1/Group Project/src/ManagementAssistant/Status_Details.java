    private void dataAdder(String applicationID) {
        DBOperations dbops = new DBOperations();
        ResultSet rs = dbops.currentStatusData(applicationID);
        try {
            rs.next();
            txtAppId.setText(applicationID);
            txtCurState.setText(rs.getString("current_state"));
            String status = rs.getString("status");
            if (status.equalsIgnoreCase("Pending")) {
                lblStatus.setText("Pending");
                lblStatus.setForeground(Color.orange);
            } else if (status.equalsIgnoreCase("Approved")) {
                lblStatus.setText("Approved");
                lblStatus.setForeground(Color.green);
            } else {
                lblStatus.setText("Reject");
                lblStatus.setForeground(Color.red);
                txtAreaReasons.setText(rs.getString("reason"));
            }
            rs = dbops.getApplication(applicationID, 0);
            rs.next();
            txtNIC.setText(rs.getString("owner_nic"));
            rs = dbops.getApplicant(txtNIC.getText(), 0);
            rs.next();
            txtInitialName.setText(rs.getString("init_name"));
            txtLastName.setText(rs.getString("last_name"));
        } catch (Exception e) {
        }

    }

