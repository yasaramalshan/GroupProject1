    private void btnPrevActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrevActionPerformed
        activePanel--;
        switch (activePanel) {
            case 1:
                disableAllPanels();
                pan1.setVisible(true);
                break;
            case 2:
                disableAllPanels();
                pan2.setVisible(true);
                break;
            case 3:
                disableAllPanels();
                pan3.setVisible(true);
                break;
            case 4:
                disableAllPanels();
                pan4.setVisible(true);
                break;
        }
        if (activePanel == 1) {
            btnPrev.hide();
        } else {
            btnPrev.show();
        }
        if (activePanel == 4) {
            btnNext.hide();
            btnSubmit.show();
        } else {
            btnSubmit.hide();
            btnNext.show();
        }
    }//GEN-LAST:event_btnPrevActionPerformed

    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        activePanel++;
        switch (activePanel) {
            case 1:
                disableAllPanels();
                pan1.setVisible(true);
                break;
            case 2:
                disableAllPanels();
                pan2.setVisible(true);
                break;
            case 3:
                disableAllPanels();
                pan3.setVisible(true);
                break;
            case 4:
                disableAllPanels();
                pan4.setVisible(true);
                break;

        }
        if (activePanel == 4) {
            btnNext.hide();
            btnSubmit.show();
        } else {
            btnSubmit.hide();
            btnNext.show();
        }
        if (activePanel == 1) {
            btnPrev.hide();
        } else {

            btnPrev.show();
        }
    }//GEN-LAST:event_btnNextActionPerformed

